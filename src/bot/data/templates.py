# Список всех категорий
CATEGORIES = list(TEMPLATES.keys())


def get_templates_by_category(category: str) -> list[dict]:
    """
    Получить шаблоны по категории.

    Args:
        category: Название категории.

    Returns:
        Список шаблонов или пустой список.
    """
    return TEMPLATES.get(category, [])


def get_template_by_id(template_id: str) -> dict | None:
    """
    Получить шаблон по ID.

    Args:
        template_id: Уникальный ID шаблона.

    Returns:
        Шаблон или None.
    """
    for templates in TEMPLATES.values():
        for template in templates:
            if template["id"] == template_id:
                return template
    return None


def get_all_templates_count() -> int:
    """
    Получить общее количество шаблонов.

    Returns:
        Количество шаблонов.
    """
    return sum(len(templates) for templates in TEMPLATES.values())
